def minimum_mewtations(start, goal, limit):
    """A diff function that computes the edit distance from START to GOAL.
    This function takes in a string START, a string GOAL, and a number LIMIT.
    Arguments:
        start: a starting word
        goal: a goal word
        limit: a number representing an upper bound on the number of edits
    >>> big_limit = 10
    >>> minimum_mewtations("cats", "scat", big_limit)       # cats -> scats -> scat
    2
    >>> minimum_mewtations("purng", "purring", big_limit)   # purng -> purrng -> purring
    2
    >>> minimum_mewtations("ckiteus", "kittens", big_limit) # ckiteus -> kiteus -> kitteus -> kittens
    3
    """
    if limit < 0:
        return 1
    
    if start == "" or goal == "":
        return abs(len(start)- len(goal))

    next, add, rev, sub = limit+1,limit+1,limit+1,limit+1#如果一切顺利，则会直接返回limit+1
    
    if start[0] == goal[0]:#这个就是对比的核心
        next = minimum_mewtations(start[1:], goal[1:], limit)
    else:
        add = 1 + minimum_mewtations(start, goal[1:], limit-1)
        rev = 1 + minimum_mewtations(start[1:], goal, limit-1)
        sub = 1 + minimum_mewtations(start[1:], goal[1:], limit-1)
        
    return min(next, add, rev, sub)
    


